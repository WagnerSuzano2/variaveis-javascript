let x = 10, y = 2;
let resultado = x + y;
console.log(`a soma de ${x} + ${y} é ${resultado}`); //12

resultado = x - y;
console.log(`a subtração de ${x} - ${y} é ${resultado}`); //8

resultado = x * y;
console.log(`a multiplicação de ${x} x ${y} é ${resultado}`); //20

resultado = x / y;
console.log(`a divisão de ${x} / ${y} é ${resultado}`); //5
